#Ejercicio 4: Dados dos (2) números calcule la suma, resta, multiplicación, división y módulo.

numero1 = int(input("Ingrese el número 1: "))
numero2 = int(input("Ingrese el número 2: "))
suma = numero1 + numero2
resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2
modulo = numero1 % numero2
print("El resultado de la suma es: " + str(suma))
print("El resultado de la resta es: " + str(resta))
print("El resultado de la multiplicación es: " + str(multiplicacion))
print("El resultado de la división es: " + str(division))
print("El resultado de el módulo es: " + str(modulo))